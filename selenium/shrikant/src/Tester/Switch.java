package Tester;

public class Switch 
{
	public static void main(String [] args) 
	{
		int number  = 20;
		
		switch(number) {
		case 20:
			System.out.println("10");
			break;
			
		case 2:
			System.out.println(20);			
			break;
			
			default:
				System.out.println("this is even number");
		}
		
	}

}
